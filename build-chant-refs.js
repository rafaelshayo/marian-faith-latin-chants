// Builds chant-refs.js: a compact map of chant ID -> primary scripture reference, extracted from
// gregorian-propers.js (temporal) and gregorian-saints.js (sanctoral).  The Propers tool loads this
// map so it can show the Swahili of each sung chant.  Run: node build-chant-refs.js
var fs = require("fs");

function load(file, varName) {
  return new Function("var " + varName + ";" + fs.readFileSync(file, "utf8") + "; return " + varName + ";")();
}

var temporal = load("gregorian-propers.js", "gregorianPropers");
var sanctoral = load("gregorian-saints.js", "gregorianPropers");
var parts = ["in", "gr", "tr", "al", "of", "co", "seq"];
var idx = {};
var count = 0;

[temporal, sanctoral].forEach(function(days) {
  for(var k in days) {
    var d = days[k];
    parts.forEach(function(p) {
      var id = d[p + "ID"], ref = d[p + "Ref"];
      // Some chants list alternate verses as an array; keep the first non-empty reference.
      if(Array.isArray(ref)) ref = ref.filter(function(r) { return r; })[0];
      if(id && ref) {
        var ids = Array.isArray(id) ? id : [id];
        ids.forEach(function(one) { if(!idx[one]) { idx[one] = ref; count++; } });
      }
    });
  }
});

fs.writeFileSync("chant-refs.js",
  "// Auto-generated by build-chant-refs.js: maps a chant ID to its primary scripture reference,\n" +
  "// so the Propers tool can show the Swahili of each sung chant.  Regenerate if the propers data changes.\n" +
  "var chantRefs = " + JSON.stringify(idx) + ";\n", "utf8");
console.info("wrote chant-refs.js with " + count + " ID->ref mappings");
